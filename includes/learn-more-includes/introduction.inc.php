	<div id="item3">
		<h3>Origen de la Mecanografía</h3>
		<hr />
		<ol>
			<li>
				<span class="text-warning fw-bold"
					>Invención de la Máquina de Escribir:</span
				>
				La <span class="text-success fw-bold">mecanografía</span> como habilidad
				surge con la invención de la máquina de escribir en el siglo XIX. La
				primera máquina de escribir comercialmente exitosa fue desarrollada por
				Christopher Latham Sholes, Samuel Soule y Carlos Glidden en 1868. Esta
				máquina permitía escribir de manera más rápida y legible que la
				escritura a mano, lo que llevó a la popularización de la
				<span class="text-success fw-bold">mecanografía</span> como habilidad
				necesaria.
			</li>
			<li>
				<span class="text-warning fw-bold"
					>Desarrollo de Métodos de Aprendizaje:</span
				>
				Con la aparición de la máquina de escribir, se hizo evidente la
				necesidad de desarrollar técnicas para aprender a usarla de manera
				eficiente. Surgieron diversos métodos de enseñanza de
				<span class="text-success fw-bold">mecanografía</span> que enseñaban la
				disposición de las teclas y la técnica adecuada para escribir
				rápidamente en el teclado.
			</li>
		</ol>
		<h5>Evolución de las Técnicas de Mecanografía</h5>
		<ol>
			<li>
				<span class="text-warning fw-bold">Estándar QWERTY:</span> La
				disposición estándar de las teclas en los teclados de las máquinas de
				escribir y computadoras, conocida como QWERTY, fue diseñada para evitar
				que las teclas se atasquen al escribir rápidamente. Este diseño, creado
				por Sholes en la máquina de escribir original, se convirtió en el
				estándar y todavía se utiliza en la mayoría de los teclados hoy en día.
			</li>
			<li>
				<span class="text-warning fw-bold"
					>Desarrollo de Métodos de Enseñanza:</span
				>
				A lo largo del tiempo, se han desarrollado diversos métodos y programas
				de enseñanza de
				<span class="text-success fw-bold">mecanografía</span> para ayudar a las
				personas a aprender a escribir más rápido y con mayor precisión. Estos
				métodos incluyen ejercicios prácticos, juegos interactivos y técnicas de
				posicionamiento de manos y dedos en el teclado.
			</li>
			<li>
				<span class="text-warning fw-bold">Incorporación en la Educación:</span>
				La <span class="text-success fw-bold">mecanografía</span> se convirtió
				en parte del currículo escolar en muchos países, especialmente a partir
				del siglo XX, donde se reconocía su importancia para el desarrollo de
				habilidades en la era de las computadoras.
			</li>
		</ol>
		<h5>Impacto Actual de la Mecanografía</h5>
		<p>
			Hoy en día, aunque la tecnología ha evolucionado con teclados virtuales y
			pantallas táctiles, la mecanografía sigue siendo una habilidad esencial
			para la eficiencia y la productividad en el trabajo y la comunicación
			digital. Las técnicas de
			<span class="text-success fw-bold">mecanografía</span> continúan
			evolucionando con programas y aplicaciones modernas que enseñan y mejoran
			esta habilidad de forma interactiva y divertida.
		</p>
