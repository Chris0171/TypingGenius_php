	<div id="item4">
		<h3>Hitos Importantes en la Historia de la Mecanografía</h3>
		<hr />
		<ol>
			<li>
				<span class="text-warning fw-bold"
					>Invención de la Máquina de Escribir (1868):</span
				>
				La invención de la máquina de escribir por Christopher Latham Sholes,
				Samuel Soule y Carlos Glidden en 1868 marcó el inicio de la era de la
				mecanografía. Esta máquina revolucionó la forma en que se escribía y
				facilitó la producción de documentos escritos de manera más rápida y
				legible.
			</li>
			<li>
				<span class="text-warning fw-bold">Estándar QWERTY (1873):</span> La
				disposición estándar de teclado QWERTY fue patentada en 1873 por
				Christopher Latham Sholes. Esta disposición de teclas se diseñó para
				evitar que las teclas se atascaran al escribir rápidamente en las
				máquinas de escribir. El diseño QWERTY se convirtió en el estándar y
				todavía se utiliza en la mayoría de los teclados hoy en día.
			</li>
			<li>
				<span class="text-warning fw-bold"
					>Desarrollo de Métodos de Enseñanza de Mecanografía
					(1880s-1900s):</span
				>
				Durante finales del siglo XIX y principios del XX, se desarrollaron
				diversos métodos de enseñanza de mecanografía para ayudar a las personas
				a aprender a usar las máquinas de escribir de manera eficiente. Estos
				métodos incluían la enseñanza de la posición correcta de las manos y los
				dedos sobre el teclado y ejercicios prácticos para mejorar la velocidad
				y precisión.
			</li>
			<li>
				<span class="text-warning fw-bold"
					>Incorporación de la Mecanografía en la Educación (1900s):</span
				>
				A medida que las máquinas de escribir se volvieron más comunes en
				oficinas y negocios, la mecanografía se incorporó en los currículos
				escolares en muchas partes del mundo. Se reconocía que la mecanografía
				era una habilidad valiosa y necesaria para futuras carreras y para
				mejorar la eficiencia en la producción de documentos.
			</li>
			<li>
				<span class="text-warning fw-bold"
					>Desarrollo de Software de Enseñanza de Mecanografía
					(1980s-presente):</span
				>
				Con la llegada de las computadoras personales, se desarrollaron
				programas de software especializados en enseñanza de mecanografía. Estos
				programas utilizaban métodos interactivos y juegos para ayudar a los
				usuarios a mejorar sus habilidades de mecanografía de manera efectiva.
			</li>
			<li>
				<span class="text-warning fw-bold"
					>Avances en Tecnología de Teclados (siglo XXI):</span
				>
				En el siglo XXI, hemos visto avances significativos en la tecnología de
				teclados, desde teclados ergonómicos hasta teclados virtuales en
				pantallas táctiles. Estos avances han cambiado la forma en que
				interactuamos con los teclados, pero la habilidad fundamental de
				mecanografía sigue siendo relevante y necesaria.
			</li>
		</ol>
		<p>
			Estos hitos representan momentos clave en la historia de la mecanografía,
			desde sus inicios con la invención de la máquina de escribir hasta los
			avances tecnológicos modernos. La mecanografía ha evolucionado con el
			tiempo, pero su importancia como habilidad esencial para la comunicación y
			la productividad sigue siendo relevante en la era digital actual.
		</p>
